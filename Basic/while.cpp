#include<stdio.h>

int main()
{
    // initialization
    int i = 0;
    
    // while() loop
    while(i > 0)  // condition
    {
        // loop body
        // instructions
        printf("Scaler Topics - Do While Loop in C");
        
        // update expression
        i++;
    }
    
    // statements outside loop
    printf("\nOut of loop");
    
    return 0;
}